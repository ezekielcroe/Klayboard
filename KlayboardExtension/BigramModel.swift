// BigramModel.swift
// English letter-pair frequency model for context-sensitive key targeting.
//
// Contains a 26×26 table of conditional log-probabilities: log(P(next | prev)).
// After the user types a letter, this biases the touch model toward keys that
// commonly follow that letter, and away from keys that rarely do.
//
// CRITICAL: This is NOT autocorrect. No word is ever changed, no suggestion appears.
// It only adjusts which key gets credit for an AMBIGUOUS touch that falls on the
// exact boundary between two keys. If the user clearly hits a key, geometry wins.
//
// Data source: English letter bigram frequencies (Norvig/Mayzner corpus).
// Size: ~5KB compiled. Zero network access. Zero runtime allocation.

